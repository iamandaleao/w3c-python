# Classificar lista alfanumericamente

# Os objetos de lista têm um sort() que classificará a lista alfanumericamente, em ordem crescente, por padrão:

thislist = ["orange", "manga", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

# Retorno: ['banana', 'kiwi', 'mango', 'orange', 'pineapple']


