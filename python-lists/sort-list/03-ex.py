# Para classificar em ordem decrescente, use o argumento de palavra-chave reverse = True:

# Classifique a lista em ordem decrescente:

thislist = ["orange", "manga", "kiwi", "pineapple", "banana"]
thislist.sort(reverse = True)
print(thislist)

# Retorno: ['pineapple', 'orange', 'mango', 'kiwi', 'banana']


